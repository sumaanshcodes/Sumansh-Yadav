
//Q. -  Write a java program to print all alphabets from a to z - using loop.


public class all_alphabets {
    public static void main(String[] args) {
        for (char c = 'a'; c <= 'z'; c++) {
            System.out.print(c + " ");
        }
        System.out.println();
        for (char c = 'A'; c <= 'Z'; c++) {
            System.out.print(c + " ");
        }


    }
}
